package convert

import "testing"

func TestBytesToUint64(t *testing.T) {
	t.Run("test range", func(t *testing.T) {
		testData := map[uint64][8]byte{
			0x00000000: {0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
			0x00000001: {0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
			0x00000002: {0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
			0x00000003: {0x03, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
			0x00000004: {0x04, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
			0x00000005: {0x05, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00},
		}
		for k, v := range testData {
			if k != BytesToUint64(v) {
				t.Fatal("failed with k:", k, "v:", v)
			}
		}
	})
}
